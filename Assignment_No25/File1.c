#include<stdio.h>

void DisplayASCII()
{
    int iCnt=0;
    printf("===========================================================================================\n");
    printf("============================================ASCII Table==================================\n");
    printf("===========================================================================================\n");

    for(iCnt=0; iCnt<=127; iCnt++)
    {
        printf("%c\t", iCnt);
        printf("%d\t", iCnt);
        printf("%o\t", iCnt);
        printf("%x\t", iCnt);
        printf("\n");
    }
    printf("===========================================================================================\n");
}

int main()
{
    DisplayASCII();
    return 0;
}